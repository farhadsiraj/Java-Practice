public class WholeNumbers {
    public static void main(String[] args) {
        // Int variables can hold a range from -2 billion to +2 billion
        int numberOfFans = 5000;
        System.out.println("There are " + numberOfFans + " fans at the Quidditch games.");

        // Long variables can hold a range from -9 quintillion to +9 quintillion
        // Values of long variables need an "L" at the end
        long globalPopulation = 7000000000L;
        System.out.println("There are " + globalPopulation + " people on earth.");
    }
}
